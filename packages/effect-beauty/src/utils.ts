function HL(e) {
  return 'number' == typeof e && e % 1 == 0;
}

export function UL(e, t, i: any = 1, r: any = 1e4, n?: any) {
  if (e < i || e > r || (n && !HL(e))) {
    throw new QD(
      Errors.INVALID_PARAMS,
      'invalid '
        .concat(t, ': the value range is [')
        .concat(i, ', ')
        .concat(r, ']. integer only')
    );
  }
}

export function ML(e, t, i) {
  if (i.includes(e)) return;
  throw new QD(
    Errors.INVALID_PARAMS,
    ''.concat(t, ' can only be set as ').concat(JSON.stringify(i))
  );
}

export class QD extends Error {
  code = undefined;
  message = undefined;
  data = undefined;
  name = 'RTCException';

  constructor(code: any, err: any = '', data?: any) {
    super(err);

    this.code = code;
    this.message = 'AgoraRTCError '.concat(this.code, ': ').concat(err);
    this.data = data;
  }
  toString() {
    return this.data
      ? 'data: '.concat(JSON.stringify(this.data), '\n').concat(this.stack)
      : ''.concat(this.stack);
  }
  print() {
    const code = this.code ?? 'error';

    if (code === 'error') {
      console.error(this.toString());
    }
    if (code === 'warning') {
      console.warn(this.toString());
    }

    return this;
  }
  throw() {
    this.print();

    throw this;
  }
}

